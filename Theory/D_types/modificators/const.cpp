/*
 * Квалификатор: const
 * Назначение:   объявление константных (неизменяемых) переменных.
 *               Значение должно быть задано при инициализации,
 *               попытка изменить его после этого приведёт к ошибке компиляции.
 * Особенности:  - const-переменные можно использовать для защиты данных от случайного изменения.
 *               - Компилятор может оптимизировать код, зная, что значение не меняется.
 *               - Для указателей возможны варианты: const int*, int* const, const int* const.
 *                 const int*         – указатель на константные данные (нельзя менять данные через указатель).
 *                 int* const         – константный указатель (нельзя изменить адрес, но данные менять можно).
 *                 const int* const   – константный указатель на константные данные.
 */

#include <iostream>
using namespace std;

int main() {
    const double PI = 3.1415926535;      // константа
    // PI = 3.14;                        // ошибка компиляции: нельзя изменить const

    int x = 10;
    const int *ptr_to_const = &x;        // указатель на константые данные
    // *ptr_to_const = 20;               // ошибка: данные константны
    int y = 30;
    ptr_to_const = &y;                   // можно: сам указатель не const

    int *const const_ptr = &x;           // константный указатель
    *const_ptr = 50;                     // можно: данные можно менять
    // const_ptr = &y;                   // ошибка: нельзя изменить адрес

    const int *const const_both = &x;    // и указатель, и данные константны
    // *const_both = 40;                 // ошибка
    // const_both = &y;                  // ошибка

    cout << "PI = " << PI << endl;
    cout << "x = " << x << endl;         // x изменилось через const_ptr до 50
    
    // Модификатор const можно применять для ссылочных параметров функций.
    return 0;
    
}
