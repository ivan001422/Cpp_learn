/*
 * Тип: int
 * Размер: обычно 4 байта (зависит от платформы, минимум 2 байта)
 * Диапазон: от –2 147 483 648 до 2 147 483 647 (для 4 байт)
 * Назначение: хранение целых чисел (счётчики, индексы, целочисленная арифметика)
 * Хранение в битах: знаковое представление в дополнительном коде.
 *   Старший бит отвечает за знак (0 — положительное, 1 — отрицательное).
 */

#include <iostream>
using namespace std;

int main() {
    int age = 25;                 // объявление и инициализация
    cout << "int переменная: " << age << endl;
    cout << "Размер int: " << sizeof(age) << " байт" << endl;
    cout << "Максимальное значение int: " << INT_MAX << endl;
    cout << "Минимальное значение int: " << INT_MIN << endl;
    return 0;
}
