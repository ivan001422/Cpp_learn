/*
 * Тип: char (символьный тип)
 * Размер: 1 байт
 * Диапазон: зависит от знаковости (обычно –128 … 127 или 0 … 255)
 *   Стандарт не определяет, является ли char знаковым или беззнаковым,
 *   но на большинстве платформ он знаковый (signed char).
 * Назначение: хранение отдельных символов (букв, цифр, знаков препинания),
 *   также может использоваться для работы с небольшими целыми числами.
 * Хранение в битах: хранит числовой код символа (обычно ASCII).
 *   Например, 'A' кодируется числом 65.
 */

#include <iostream>
using namespace std;

int main() {
    char letter = 'A';            // символ
    char digit = '7';             // символ цифры, а не число 7
    cout << "char переменная: " << letter << endl;
    cout << "Размер char: " << sizeof(letter) << " байт" << endl;
    cout << "Код символа 'A': " << int(letter) << endl;
    cout << "Символ цифры '7' как число: " << int(digit) - int('0') << endl;
    return 0;
}
