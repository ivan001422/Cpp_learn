#include <iostream>
using namespace std;

/*
 * C-style перечисления (enum)
 *
 * enum — это пользовательский тип, состоящий из набора
 * именованных целочисленных констант.
 *
 * Особенности обычного enum:
 *  - Имена констант находятся в той же области видимости,
 *    что и само перечисление (могут конфликтовать).
 *  - Значения неявно преобразуются в int.
 *  - По умолчанию первый элемент равен 0, каждый следующий +1.
 *  - Можно явно задавать числовые значения.
 */

// Простое перечисление цветов
enum Color {
    RED,    // 0
    GREEN,  // 1
    BLUE    // 2
};

// Перечисление с явным заданием значений
enum Month {
    JAN = 1,   // 1
    FEB,       // 2  (автоматически +1)
    MAR = 5,   // 5
    APR        // 6  (автоматически +1 после 5)
};

// Перечисление часто используют для состояний, флагов, дней недели и т.п.
enum Day {
    MON, TUE, WED, THU, FRI, SAT, SUN
};

int main() {
    // Объявление переменной enum-типа
    Color favorite = BLUE;
    Day today = WED;

    // enum-значения можно сравнивать, использовать в switch
    if (favorite == BLUE) {
        cout << "Любимый цвет - синий\n";
    }

    // Неявное приведение к int
    int day_number = today;   // day_number = 2 (т.к. WED = 2)
    cout << "Номер дня: " << day_number << endl;

    // switch с enum
    switch (today) {
        case MON: cout << "Понедельник\n"; break;
        case TUE: cout << "Вторник\n"; break;
        case WED: cout << "Среда\n"; break;
        default:  cout << "Другой день\n";
    }

    // Имена вне enum не изолированы – возможен конфликт:
    // int RED = 10;   // ошибка, RED уже определён в enum Color!

    // Вывод значений
    cout << "Месяцы: JAN=" << JAN << ", FEB=" << FEB
         << ", MAR=" << MAR << ", APR=" << APR << endl;

    // ---------------------------------------------------------
    // Пример использования enum с C-массивами
    // ---------------------------------------------------------

    // 1) Enum как индекс (после неявного приведения к int)
    const char* dayNames[] = {"Пн", "Вт", "Ср", "Чт", "Пт", "Сб", "Вс"};
    // today имеет значение WED (2), поэтому dayNames[2] = "Ср"
    cout << "Сегодня: " << dayNames[today] << endl;

    // 2) Массив enum-значений
    Color palette[3] = {RED, GREEN, BLUE};
    cout << "Первый цвет палитры: ";
    switch (palette[0]) {
        case RED:   cout << "Красный\n"; break;
        case GREEN: cout << "Зелёный\n"; break;
        case BLUE:  cout << "Синий\n"; break;
    }

    return 0;
}
