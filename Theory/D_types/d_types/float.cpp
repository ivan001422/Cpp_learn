/*
 * Тип: float (число с плавающей точкой одинарной точности)
 * Размер: 4 байта
 * Диапазон: примерно от 1.2e-38 до 3.4e+38 (абсолютные значения)
 * Точность: около 6–7 значащих десятичных цифр
 * Назначение: вещественные числа, когда важна скорость и экономия памяти
 *   (графика, игры, простые физические расчёты)
 * Хранение в битах: стандарт IEEE 754:
 *   1 бит знака, 8 бит экспоненты, 23 бита мантиссы.
 */

#include <iostream>
using namespace std;

int main() {
    float temperature = 36.6f;   // суффикс 'f' указывает, что литерал — float
    cout << "float переменная: " << temperature << endl;
    cout << "Размер float: " << sizeof(temperature) << " байт" << endl;
    cout << "Пример вычисления: 1.0f / 3.0f = " << 1.0f / 3.0f << endl;
    return 0;
}
