#include <iostream>
using namespace std;

/*
 * Арифметические операции: +, -, *, /, %
 *
 * Важно:
 * - Умножение и деление: если оба операнда целые, результат целый.
 *   Если хотя бы один операнд дробный (float, double), результат дробный.
 * - Деление целых: дробная часть отбрасывается (не округление!).
 * - Остаток (%) работает только с целыми операндами.
 *   Знак результата совпадает со знаком левого операнда (C++11 и новее).
 *   Пример: -10 % 3 = -1,  10 % -3 = 1.
 */

int main() {
    int a = 10, b = 3;
    double x = 10.0, y = 3.0;

    cout << "Сложение:       " << a << " + " << b << " = " << a + b << '\n';
    cout << "Вычитание:      " << a << " - " << b << " = " << a - b << '\n';

    // Умножение: целое * целое = целое, целое * double = double
    cout << "Умножение (int*int): " << a << " * " << b << " = " << a * b << '\n';
    cout << "Умножение (int*double): " << a << " * " << y << " = " << a * y << '\n';

    // Деление
    cout << "Деление (int/int): " << a << " / " << b << " = " << a / b
        << "  (дробная часть отброшена)\n";
    cout << "Деление (double/double): " << x << " / " << y << " = " << x / y << '\n';

    // Остаток от деления с отрицательными числами
    int neg_a = -10;
    cout << "Остаток: " << a << " % " << b << " = " << a % b << '\n';
    cout << "Остаток с отрицательным левым: " << neg_a << " % " << b << " = " << neg_a % b
        << "  (знак как у левого операнда)\n";
    cout << "Остаток с отрицательным правым: " << a << " % -" << b << " = " << a % -b
        << "  (знак левого операнда)\n";

    // Как вычисляется остаток (a % b)
    // Поскольку целочисленное деление a / b отбрасывает дробную часть,
    // остаток определяется как a - (a / b) * b.

    return 0;
}
