#include <iostream>
using namespace std;

/*
 * Тернарная (условная) операция: условие ? выражение1 : выражение2
 *
 * Это краткая форма записи условного выбора, аналог if-else,
 * но в виде выражения, а не инструкции.
 *
 * Если условие истинно, возвращается выражение1, иначе выражение2.
 * Оба выражения должны быть одного типа (или приводиться к общему типу).
 *
 * Производительность:
 *   В большинстве случаев компилятор генерирует для тернарного оператора
 *   тот же машинный код, что и для эквивалентного if-else.
 *   Однако тернарный оператор может быть эффективнее в сценариях,
 *   где требуется обязательно инициализировать константу или ссылку,
 *   либо вернуть значение из функции без лишних ветвлений.
 *   На уровне исходного кода он позволяет писать более компактные
 *   и выразительные конструкции.
 */

int main() {
    int a = 10, b = 20;

    // Эквивалент if-else для нахождения максимума
    int max = (a > b) ? a : b;
    cout << "max(" << a << ", " << b << ") = " << max << '\n';

    // То же самое через if-else было бы:
    // int max;
    // if (a > b) max = a; else max = b;

    // Проверка чётности
    int number = 7;
    cout << number << " – " << ((number % 2 == 0) ? "чётное" : "нечётное") << '\n';

    // Использование внутри выражения (if-else так нельзя)
    cout << "Меньшее: " << ((a < b) ? a : b) << '\n';

    // Инициализация константы (возможна только с тернарным оператором)
    const int abs_val = (number >= 0) ? number : -number;
    cout << "|" << number << "| = " << abs_val << '\n';

    return 0;
}
