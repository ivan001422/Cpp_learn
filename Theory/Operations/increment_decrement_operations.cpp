#include <iostream>
using namespace std;

/*
 * Операции инкремента (++) и декремента (--)
 *
 * Увеличивают или уменьшают значение переменной на 1.
 * Префиксная форма (++i) – сначала изменяет, потом возвращает новое значение.
 * Постфиксная форма (i++) – возвращает старое значение, потом изменяет.
 */

int main() {
    int i = 5;

    cout << "Начальное i = " << i << '\n';
    cout << "i++ (постфиксный): " << i++ << " (теперь i = " << i << ")\n";
    cout << "++i (префиксный):  " << ++i << " (теперь i = " << i << ")\n";
    cout << "i-- : " << i-- << " (теперь i = " << i << ")\n";
    cout << "--i : " << --i << " (теперь i = " << i << ")\n";

    return 0;
}
