#include <iostream>
#include <cmath>    // для fabs()
using namespace std;

/*
 * Операции сравнения (отношения): ==, !=, <, >, <=, >=
 *
 * Возвращают булево значение (true или false).
 * Сравнивают два операнда и указывают на их отношение.
 * Часто используются в условиях (if, while).
 *
 * Нюанс: прямое сравнение дробных чисел (float, double) через ==
 * может давать ложный результат из-за погрешностей округления.
 * Например, после приведения int к double всё точно, но после
 * вычислений с плавающей точкой накапливается ошибка.
 * Правильный способ: сравнивать модуль разности с малым значением (eps).
 */

int main() {
    int a = 10, b = 3;

    cout << "=== Сравнение целых чисел ===" << endl;
    cout << "a = " << a << ", b = " << b << endl;
    cout << "a == b : " << (a == b) << endl;
    cout << "a != b : " << (a != b) << endl;
    cout << "a < b  : " << (a < b)  << endl;
    cout << "a > b  : " << (a > b)  << endl;
    cout << "a <= b : " << (a <= b) << endl;
    cout << "a >= b : " << (a >= b) << endl;

    cout << "\n=== Сравнение дробных чисел ===" << endl;

    // 1. Приведение целого к double — сравнение корректно,
    //    так как целые числа точно представимы в double (до 2^53).
    double x = static_cast<double>(a);      // 10.0
    double y = 10.0;
    cout << "x = " << x << ", y = " << y << endl;
    cout << "x == y : " << (x == y) << "  (корректно, 10.0 == 10.0)" << endl;

    // 2. Опасный случай: результат вычислений с плавающей точкой
    double z = 0.1 + 0.2;   // ожидаем 0.3
    double w = 0.3;
    cout << "z = 0.1 + 0.2 = " << z << endl;
    cout << "w = " << w << endl;
    // Прямое сравнение может дать false!
    cout << "z == w : " << (z == w) << "  (может быть false из-за погрешности)" << endl;

    // 3. Правильное сравнение через эпсилон
    const double EPS = 1e-9;
    bool equal = fabs(z - w) < EPS;
    cout << "fabs(z - w) < EPS : " << equal
         << "  (корректное сравнение с точностью)" << endl;

    return 0;
}
