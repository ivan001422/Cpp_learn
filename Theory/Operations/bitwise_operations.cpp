#include <iostream>
using namespace std;

/*
 * Побитовые операции: & (И), | (ИЛИ), ^ (XOR), ~ (НЕ), << (сдвиг влево), >> (сдвиг вправо)
 *
 * Применяются только к целочисленным типам.
 * Работают на уровне отдельных битов представления числа.
 * Сдвиг влево на n бит эквивалентен умножению на 2^n. Сдвигаемые биты обращаются в нуль.
 * Сдвиг вправо на n бит эквивалентен делению на 2^n (для неотрицательных чисел). Сдвигаемые биты обращаются в нуль.
 */

int main() {
    unsigned int m = 5;  // 000...0101
    unsigned int n = 3;  // 000...0011

    cout << "m & n  : " << (m & n) << '\n';   // 1  (0101 & 0011 = 0001)
    cout << "m | n  : " << (m | n) << '\n';   // 7  (0101 | 0011 = 0111)
    cout << "m ^ n  : " << (m ^ n) << '\n';   // 6  (0101 ^ 0011 = 0110)
    cout << "~m     : " << (~m) << '\n';      // побитовое НЕ (зависит от разрядности)
    cout << "m << 1 : " << (m << 1) << '\n';  // 10 (умножение на 2)
    cout << "m >> 1 : " << (m >> 1) << '\n';  // 2  (целочисленное деление на 2)

    return 0;
}
