#include <iostream>
using namespace std;

/*
 * Логические операции: && (И), || (ИЛИ), ! (НЕ)
 *
 * Работают с булевыми операндами (или значениями, приводимыми к bool).
 * Возвращают true (1) или false (0).
 * && – истина, если оба операнда истинны.
 * || – истина, если хотя бы один истинен.
 * !  – инвертирует значение.
 */

int main() {
    bool p = true, q = false;

    cout << "p && q : " << (p && q) << '\n'; // false (0)
    cout << "p || q : " << (p || q) << '\n'; // true (1)
    cout << "!p     : " << (!p) << '\n';     // false (0)

    // Пример с числами: любое ненулевое число → true, ноль → false
    int a = 5, b = 0;
    cout << "a && b : " << (a && b) << '\n'; // 0, так как b == 0
    cout << "a || b : " << (a || b) << '\n'; // 1

    return 0;
}
