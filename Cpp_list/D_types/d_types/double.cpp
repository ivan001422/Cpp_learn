/*
 * Тип: double (число с плавающей точкой двойной точности)
 * Размер: 8 байт
 * Диапазон: примерно от 2.2e-308 до 1.8e+308 (абсолютные значения)
 * Точность: около 15–16 значащих десятичных цифр
 * Назначение: научные и инженерные расчёты, где требуется высокая точность
 *   (математическое моделирование, финансовые вычисления)
 * Хранение в битах: стандарт IEEE 754:
 *   1 бит знака, 11 бит экспоненты, 52 бита мантиссы.
 */

#include <iostream>
using namespace std;

int main() {
    double pi = 3.141592653589793;  // двойная точность, литерал по умолчанию double
    cout << "double переменная: " << pi << endl;
    cout << "Размер double: " << sizeof(pi) << " байт" << endl;
    cout << "Пример вычисления: 1.0 / 3.0 = " << 1.0 / 3.0 << endl;
    return 0;
}
