/*
 * Ключевое слово: register
 * Назначение: подсказка компилятору, что переменную желательно хранить в регистре
 *             процессора для быстрого доступа (исторически).
 * Статус:      устаревшее (deprecated в C++11, удалено как спецификатор хранения в C++17,
 *             но слово остаётся зарезервированным).
 * Особенности: - Компилятор не обязан следовать подсказке: современные оптимизаторы
 *                обычно сами эффективно распределяют регистры, но в некоторых
 *                реализациях или при отключённой оптимизации «совет» может быть учтён.
 *              - Нельзя получить адрес register-переменной (так как у неё может не быть
 *                адреса в оперативной памяти).
 *              - В большинстве случаев ручное указание register не даёт преимуществ
 *                перед автоматической оптимизацией.
 */

#include <iostream>
using namespace std;

int main() {
    register int sum = 0;          // подсказка компилятору (может быть проигнорирована)
    for (register int i = 1; i <= 10; ++i) {
        sum += i;
    }
    cout << "Сумма от 1 до 10 = " << sum << '\n';

    // Следующая строка вызвала бы ошибку:
    // int* ptr = &sum;   // нельзя взять адрес register-переменной

    return 0;
}
