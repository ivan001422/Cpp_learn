/*
 * Ключевое слово: static
 * Назначение: изменяет продолжительность хранения и связывание в зависимости
 *             от контекста.
 *
 * 1) static внутри функции (локальная статическая переменная):
 *    - Значение сохраняется между вызовами функции.
 *    - Инициализируется только один раз (при первом вызове).
 *    - Время жизни — всё время работы программы.
 *
 * 2) static на уровне файла (глобальная статическая переменная или функция):
 *    - Ограничивает видимость текущей единицей трансляции (internal linkage).
 *    - Не доступна из других .cpp файлов (в отличие от extern).
 */

#include <iostream>
using namespace std;

// Глобальная static-переменная — видна только в этом файле
static int internal_value = 999;

// Функция с локальной static-переменной
void count_calls() {
    static int call_count = 0;   // сохраняется между вызовами
    ++call_count;
    cout << "Функция вызвана " << call_count << " раз(а)\n";
}

void show_internal() {
    cout << "internal_value = " << internal_value << '\n';
}

int main() {
    count_calls();   // 1
    count_calls();   // 2
    count_calls();   // 3
    show_internal(); // 999
    return 0;
}
