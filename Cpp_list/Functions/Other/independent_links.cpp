#include <iostream>
using namespace std;

/*
 * ============================================================
 *  УКАЗАТЕЛИ И ССЫЛКИ В C++
 * ============================================================
 *
 * Указатель (pointer)   – переменная, хранящая АДРЕС другой переменной.
 * Ссылка   (reference)  – ПСЕВДОНИМ уже существующей переменной.
 *
 * Главные различия:
 * 1) Указатель можно (и часто нужно) инициализировать позже,
 *    ссылку – только сразу, при создании.
 * 2) Указатель можно перенаправить на другой объект,
 *    ссылка ВСЕГДА указывает на тот объект, с которым была создана.
 * 3) Указатель может быть nullptr, ссылка – никогда (если код корректен).
 * 4) Для доступа к значению через указатель нужна операция разыменования *p,
 *    ссылка используется как обычная переменная.
 * 5) Ссылка обычно не занимает дополнительной памяти (реализуется компилятором),
 *    указатель – занимает (размер адреса).
 */

int main() {
    // ---------- ИНИЦИАЛИЗАЦИЯ ----------
    int arr[3] = { 1, 2, 3 };

    int *p = arr;        // p получает АДРЕС первого элемента массива
    int &l = arr[0];     // l – ссылка (псевдоним) на arr[0]

    cout << "После инициализации:\n";
    cout << "p = " << p << "  (адрес arr[0])\n";
    cout << "l = " << l << "  (значение arr[0])\n\n";

    // ---------- ИЗМЕНЕНИЕ ----------
    p = &arr[1];         // указатель теперь хранит адрес arr[1]
    l = arr[1];          // ссылка НЕ перенаправляется!
    // Это ПРИСВАИВАНИЕ: arr[0] = arr[1] (теперь arr[0] == 2)

    cout << "После изменений:\n";
    cout << "p = " << p << "  (адрес arr[1])\n";
    cout << "l = " << l << "  (всё ещё arr[0], но его значение изменилось на 2)\n";
    cout << "arr[0] = " << arr[0] << '\n';
    cout << "arr[1] = " << arr[1] << "\n\n";

    // ---------- ДЕМОНСТРАЦИЯ РАЗЫМЕНОВАНИЯ ----------
    *p = 100;            // меняем arr[1] через указатель
    cout << "После *p = 100: arr[1] = " << arr[1] << '\n';

    // ---------- ОПАСНОСТИ ----------
    // Никогда не возвращайте ссылку или указатель на локальную переменную!
    // int& bad() { int local = 10; return local; }  // ОШИБКА

    return 0;
}

