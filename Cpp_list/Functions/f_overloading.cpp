#include <iostream>

using namespace std;

/*

ПЕРЕГРУЗКА ФУНКЦИЙ (FUNCTION OVERLOADING)

В C++ несколько функций могут иметь одинаковые имена, но при условии,
что их параметры различаются. Такую ситуацию называют перегрузкой функций,
а функции, которые в ней задействованы, — перегруженными.

Перегрузка функций — один из способов реализации полиморфизма в C++.

Чтобы перегрузить функцию, достаточно объявить различные её версии.
Об остальном позаботится компилятор.

Важно помнить:
• Тип и/или количество параметров каждой перегруженной функции должны быть уникальными.
• Если две функции отличаются только типом возвращаемого значения, то этой информации
  недостаточно, чтобы компилятор мог определить, какую версию вызвать.
• Конечно, такие функции могут отличаться и типами возвращаемых значений.

При вызове перегруженной функции будет вызвана та функция, чьи параметры
соответствуют заданным аргументам.

*/

int del(int, int);      // Целочисленная версия
double del(double, double); // Версия с плавающей точкой

int main() {
    cout << del(10, 5) << '\n';   // Вызовет int-версию: 10 / 5 = 2
    cout << del(0.5, 0.1);        // Вызовет double-версию: 0.5 / 0.1 = 5
}

int del(int x, int y) {
    return x / y;
}

double del(double x, double y) {
    return x / y;
}

/*

ПРЕИМУЩЕСТВО ПОЛИМОРФИЗМА:

Благодаря полиморфизму нам нужно помнить не два различных имени функции,
а только одно — del(). Компилятор сам определяет, какую версию вызвать,
основываясь на типах переданных аргументов.

*/
